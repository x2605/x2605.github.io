    var reg;
    for(var key in lang){
        reg = new RegExp(key, 'i');
        if(navigator.language.match(reg)){
            var doms = document.querySelectorAll('[data-langKey]');
            for(var i = 0; i < doms.length; i++){
                doms[i].innerHTML = lang[key][doms[i].dataset['langkey']]
            };
            break;
        }
    }
